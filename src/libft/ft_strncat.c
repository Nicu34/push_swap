#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		j;

	j = 0;
	while (s1[j])
		j++;
	i = 0;
	while (s2[i] && i < n)
		s1[j++] = s2[i++];
	return (s1);
}
