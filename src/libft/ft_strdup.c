#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s;
	int		i;

	s = (char*)malloc(sizeof(char) * ft_strlen(s1) + 1);
	if (sizeof(s1) != sizeof(s))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s[i] = s1[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
